package com.dunkworld.learn.model;

public class TradeRequest {
	
	private String symbol; 
	private int size;
	
	
	public String getSymbol() {
		return symbol;
	}
	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}
	public int getSize() {
		return size;
	}
	public void setSize(int size) {
		this.size = size;
	} 
	
	
	

}
